    private void tablaResultadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaResultadoMouseClicked
        // TODO add your handling code here:
 
        int fila = this.tablaResultado.getSelectedRow();
        this.txtId.setText(this.tablaResultado.getValueAt(fila,0).toString());
        this.txtPalabra.setText(this.tablaResultado.getValueAt(fila, 1).toString());
        this.txtDefinicion.setText(this.tablaResultado.getValueAt(fila, 2).toString());
        this.txtEjemplos.setText(this.tablaResultado.getValueAt(fila, 3).toString());               
    }//GEN-LAST:event_tablaResultadoMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        Palabra palabra = new Palabra(Integer.parseInt(this.txtId.getText()),this.txtPalabra.getText(),this.txtDefinicion.getText(),this.txtEjemplos.getText());
        String mensaje=palabra.Actualizar();
        JOptionPane.showMessageDialog(this, mensaje, "Informaci贸n", JOptionPane.INFORMATION_MESSAGE);
        System.out.println("mensajeU:" + mensaje);
        cargarDefault();
    }//GEN-LAST:event_btnUpdateActionPerformed

